package cn.edu.nju.gqx.bean;

public class MapAttributeBean {
	private String mapId;
	private int realHeight;
	private int realWidth;
	private String xmlPath;
	private String bgImgPath;
	private String sysname;
	private String treeXmlPath;
	public String getMapId() {
		return mapId;
	}
	public void setMapId(String mapId) {
		this.mapId = mapId;
	}
	public int getRealHeight() {
		return realHeight;
	}
	public void setRealHeight(int realHeight) {
		this.realHeight = realHeight;
	}
	public int getRealWidth() {
		return realWidth;
	}
	public void setRealWidth(int realWidth) {
		this.realWidth = realWidth;
	}
	public String getXmlPath() {
		return xmlPath;
	}
	public void setXmlPath(String xmlPath) {
		this.xmlPath = xmlPath;
	}
	public String getBgImgPath() {
		return bgImgPath;
	}
	public void setBgImgPath(String bgImgPath) {
		this.bgImgPath = bgImgPath;
	}
	public String getSysname() {
		return sysname;
	}
	public void setSysname(String sysname) {
		this.sysname = sysname;
	}
	public String getTreeXmlPath() {
		return treeXmlPath;
	}
	public void setTreeXmlPath(String treeXmlPath) {
		this.treeXmlPath = treeXmlPath;
	}	
	
}
